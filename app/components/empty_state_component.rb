# frozen_string_literal: true

class EmptyStateComponent < ViewComponent::Base
  def initialize(
    title:,
    message:,
    icon_name: nil,
    icon_path: nil,
    action_text: nil,
    action_url: nil,
    action_variant: :primary,
    variant: :default,
    **options
  )
    @title = title
    @message = message
    @icon_name = icon_name
    @icon_path = icon_path
    @action_text = action_text
    @action_url = action_url
    @action_variant = action_variant
    @variant = variant
    @options = options
  end

  private

  attr_reader :title, :message, :icon_name, :icon_path, :action_text, :action_url, :action_variant, :variant, :options

  def container_classes
    case variant
    when :simple
      "px-4 py-4 sm:px-6 text-sm text-gray-500 dark:text-gray-400"
    else
      "text-center py-12"
    end
  end

  def show_icon?
    variant != :simple && (icon_name.present? || icon_path.present?)
  end

  def icon_svg_path
    return icon_path if icon_path.present?

    predefined_icons[icon_name.to_sym] if icon_name.present?
  end

